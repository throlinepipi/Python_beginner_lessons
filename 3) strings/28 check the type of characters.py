'''
isalnum() -- alphanumeric
isalpha() -- alphabet
isdigit() -- digit
islower() -- lowercase
isupper() -- upper
istitle()  -- title
isspace() -- whitespace
'''

print("Myanmar2022".isalnum())
print("Myanmar".isalnum())
print("2022".isalnum())
print("Myanmar2022".isalpha())
print("Myanmar2022".isdigit())
print("Myanmar".isdigit())
print("        ".isspace())
print("Myanmar".isspace())